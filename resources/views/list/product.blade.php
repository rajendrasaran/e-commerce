<script>
	// JavaScript code to filter products based on user selection
	document.getElementById('color-select').addEventListener('change', filterProducts);
	document.getElementById('size-select').addEventListener('change', filterProducts);

	function filterProducts() {
		const selectedColor = document.getElementById('color-select').value;
		const selectedSize = document.getElementById('size-select').value;

		const products = document.querySelectorAll('.product');
		products.forEach(product => {
			const color = product.getAttribute('data-color');
			const size = product.getAttribute('data-size');

			if (
				(selectedColor === 'all' || selectedColor === color) &&
				(selectedSize === 'all' || selectedSize === size)
			) {
				product.style.display = 'block';
			} else {
				product.style.display = 'none';
			}
		});
	}
</script>
